def addNumber(x,y):
    print(x,"+",y,"=",x+y)

def miNumber(x,y):
    print(x,"-",y,"=",x-y)

def mulNumber(x,y):
    print(x,"*",y,"=",x*y)

def diNumber(x,y):
    print(x,"/",y,"=",x/y)

x = int(input("Number1 = "))
y = int(input("Number2 = "))
addNumber(x,y)
miNumber(x,y)
mulNumber(x,y)
diNumber(x,y)
